# Creation of main board and copy board of 47 x 17
# Main board is used for the potrayal of the game and is always the finalized version of the board per round
# Copy board is used for editing the game behind the scenes, so printing each round of life is cohesive
main_board=[[0 for x in range(47)] for y in range(17)] 
copy_board=[[0 for x in range(47)] for y in range(17)] 

# Dictionary that shows which positions needs to be checked per the cell's location
position_case_dict={
"NW":["E","SE","S"],
"N":["E","W","SE","S","SW"],
"NE":["W","SW","S"],
"W":["N","NE","E","SE","S"],
"M":["N","NE","E","SE","S","W","NW","SW"],
"E":["N","NW","W","SW","S"],
"SW":["N","NE","E"],
"S":["W","NW","N","NE","E"],
"SE":["W","NW","N"]}

# Check the position of the cell - returns list of positions that need to be checked by neighbors_check function
def get_classification(pos):
    if pos[0]==0:
        if pos[1]==0:
            return position_case_dict["NW"]
        elif pos[1]==len(copy_board[0])-1:
            return position_case_dict["NE"]
        else:
            return position_case_dict["N"]
    if pos[0]==len(copy_board)-1:
        if pos[1]==0:
            return position_case_dict["SW"]
        elif pos[1]==len(copy_board[0])-1:
            return position_case_dict["SE"]
        else:
            return position_case_dict["S"]
    if pos[1]==0:
        return position_case_dict["W"] 
    elif pos[1]==len(copy_board[0])-1:
        return position_case_dict["E"] 
    else:
        return position_case_dict["M"]

# Checking all alive neighbors of cell depending on return item from get_classification (the cell's position)
def neighbors_check(pos):
    alive_count = 0
    alive_list = get_classification(pos)
    for item in alive_list:
        if item == "NW":
            if copy_board[pos[0]-1][pos[1]-1].state==1:
                alive_count+=1
        if item == "N":
            if copy_board[pos[0]-1][pos[1]].state==1:
                alive_count+=1
        if item == "NE":
            if copy_board[pos[0]-1][pos[1]+1].state==1:
                alive_count+=1
        if item == "W":
            if copy_board[pos[0]][pos[1]-1].state==1:
                alive_count+=1
        if item == "E":
            if copy_board[pos[0]][pos[1]+1].state==1:
                alive_count+=1
        if item == "SW":
            if copy_board[pos[0]+1][pos[1]-1].state==1:
                alive_count+=1
        if item == "S":
            if copy_board[pos[0]+1][pos[1]].state==1:
                alive_count+=1
        if item == "SE":
            if copy_board[pos[0]+1][pos[1]+1].state==1:
                alive_count+=1
    return alive_count
	
# Cell class for containing its state (alive or dead) and function to change its state called every loop
class cell:
    def __init__(self, state, position):
        self.state = state
        self.position = position
    def change_state(self):
        num_neigh_alive = neighbors_check(self.position)
        # Changing cell per rules of the game
        if (num_neigh_alive < 2 or num_neigh_alive > 3) and self.state == 1:
            self.state = 0
        elif num_neigh_alive == 3 and self.state == 0:
            self.state = 1

